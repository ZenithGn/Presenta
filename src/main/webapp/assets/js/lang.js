function translateNode(node, lang) {
    if (node.nodeType === Node.TEXT_NODE) {
        let text = node.nodeValue.trim();
        if (text === '') return;

        // Save original English
        if (!node.originalText) {
            node.originalText = node.nodeValue;
        }

        let originalTrimmed = node.originalText.trim();

        if (lang === 'vi') {
            if (dict[originalTrimmed]) {
                node.nodeValue = node.originalText.replace(originalTrimmed, dict[originalTrimmed]);
            }
        } else {
            node.nodeValue = node.originalText;
        }
    } else if (node.nodeType === Node.ELEMENT_NODE) {
        // Skip script and style tags
        if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') return;

        // Skip elements that explicitly request no translation
        if (node.classList && node.classList.contains('no-translate')) return;
        if (node.dataset && node.dataset.noTranslate) return;

        // Handle input placeholders
        if (node.tagName === 'INPUT' || node.tagName === 'TEXTAREA') {
            if (node.placeholder) {
                if (!node.dataset.origPlaceholder) {
                    node.dataset.origPlaceholder = node.placeholder;
                }
                let orig = node.dataset.origPlaceholder;
                if (lang === 'vi' && dict[orig]) {
                    node.placeholder = dict[orig];
                } else if (lang === 'en') {
                    node.placeholder = orig;
                }
            }
            if (node.type === 'submit' || node.type === 'button') {
                if (node.value) {
                    if (!node.dataset.origValue) {
                        node.dataset.origValue = node.value;
                    }
                    let orig = node.dataset.origValue;
                    if (lang === 'vi' && dict[orig]) {
                        node.value = dict[orig];
                    } else if (lang === 'en') {
                        node.value = orig;
                    }
                }
            }
        }

        for (let i = 0; i < node.childNodes.length; i++) {
            translateNode(node.childNodes[i], lang);
        }
    }
}

function updateToggleUI(lang) {
    const wrappers = document.querySelectorAll('.lang-toggle-switch');
    wrappers.forEach(wrapper => {
        const slider = wrapper.querySelector('.lang-slider');
        if (slider) {
            if (lang === 'vi') {
                slider.style.transform = 'translateX(30px)';
            } else {
                slider.style.transform = 'translateX(0)';
            }
        }
    });
}

function applyLanguage(lang) {
    localStorage.setItem('lang', lang);
    translateNode(document.body, lang);
    updateToggleUI(lang);
}

function toggleLanguage() {
    let current = localStorage.getItem('lang') || 'vi';
    applyLanguage(current === 'vi' ? 'en' : 'vi');
}

document.addEventListener('DOMContentLoaded', () => {
    let current = localStorage.getItem('lang') || 'vi';
    applyLanguage(current);
});
